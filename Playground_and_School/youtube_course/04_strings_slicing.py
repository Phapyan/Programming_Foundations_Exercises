#Slicing bedeutet hier die Zahl printen an der Stelle der Zeichenkette, also hier beim Wort Hallo beim H
x = "Hallo"
print(x[0])

#Der Doppelpunkt bedeutet bis und die Zahl bedeutet ab
x = "Hallo Welt"
print(x[0:3])

#Gibt nur Hall aus, da die Regel nur bis ist, nicht bis und mit.
x = "Hallo Welt"
print(x[0:4])

